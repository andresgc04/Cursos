"""
Existen tres tipos de operadores logicos que son los siguientes:

and (&&), or (||) y not (!)
"""

#and:
resultado_final = True and True and 100 > 20

print(resultado_final)

#or:
resultado_final2 = False or False or 100 > 20

print(resultado_final2)

#not:
resultado_final3 = not False

print(resultado_final3)