diccionario = {'a' : 1, 'b' : 2, 'c' : 3, 'd' : 4}

#keys
#values 
#items

#El método keys se utiliza para obtener las llaves de un diccionario.
llaves = tuple(diccionario.keys())

print(llaves)

#El método vlaues se utiliza para obtener los valores almacenados dentro de un diccionario.
valores = tuple(diccionario.values())
print(valores)

#El método items se utiliza para obtener los items o elementos de un diccionario.
elementos = tuple(diccionario.items())
print(elementos)