#Las listas contienen indices que son los que manejan las posiciones de sus elementos.
#                    0        1         2        3      4
#indices con numeros negativos se leen de izquierda a derecha y iniciando desde el -1
#                   -5        -4       -3       -2      -1
listas_cursos = ['Python', 'Django', 'Flask', 'Ruby', 'Java']

primer_curso = listas_cursos[0]
print(primer_curso)

segundo_curso = listas_cursos[1]
print(segundo_curso)

ultimo_curso = listas_cursos[4]
print(ultimo_curso)